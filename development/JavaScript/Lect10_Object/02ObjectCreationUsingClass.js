// object create using class

class perso {
    constructor(na , ag , gen){
        this.name = na;
        this.age = ag;
        this.gender =  gen;
    }
}
let per1 = new perso("him", 20 , "male");
let per2 = new perso("raj", 30 , "female");
console.log(per1);
console.log(per2);


